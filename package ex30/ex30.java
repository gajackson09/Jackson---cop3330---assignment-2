 //this program creates a simple multiplication table
 public class ex30{
    public static void main(String[] args){
        for(int i=1;i<=12;i++){
            for(int j=1;j<=12;j++){
                System.out.print((i*j)+"\t");
            }//end of for
        System.out.println();
        }//end of for
    }//end of main
}//end of class